import java.util.Scanner;
public class Main {
    public static void main(String[] args) {
        //Declaramos las variables
        int num1=10;
        int num2=5;

        /*Realizamos las operaciones.
         * Tambien lo podemos guardar el resultado en variables. */

        System.out.println("El resultado de la suma de 10 y 5 es "+(num1+num2));

        System.out.println("El resultado de la resta de 10 y 5 es "+(num1-num2));

        System.out.println("El resultado de la multiplicación de 10 y 5 es "+(num1*num2));

        System.out.println("El resultado de la división de 10 y 5 es "+(num1/num2));
    }
}